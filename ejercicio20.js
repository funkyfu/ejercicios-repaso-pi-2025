//con el metodo switch mostrar el dato que se sollicita según lo indicado en el ejercicio

let numeroDia = 1
let Dia

switch(numeroDia){
    case 1:
        Dia = "Lunes" 
        break
    case 2:
        Dia = "Martes" 
        break
    case 3:
        Dia = "Miercoles"
        break
    case 4:
        Dia = "Jueves"
        break
    case 5:
        Día = "Viernes" 
        break
    default:
        Dia = "no es un Día de la semana"
        break 
}

//con switch podemos validar varios casos asignados de una manera mas ordenada y más completa
